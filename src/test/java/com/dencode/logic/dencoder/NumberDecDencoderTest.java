	@Test
	public void test_decoder() {
		// Blank
		tester.testDecoder("", null);

		// Integer
		tester.testDecoder("0", "0");
		tester.testDecoder("1", "1");
		tester.testDecoder("10", "10");
		tester.testDecoder("100", "100");
		tester.testDecoder("99", "99");

		// Integer (-)
		tester.testDecoder("-1", "-1");
		tester.testDecoder("-10", "-10");
		tester.testDecoder("-100", "-100");

		// Integer (+)
		tester.testDecoder("+1", "1");
		tester.testDecoder("+10", "10");

		// Decimal
		tester.testDecoder("1.1", "1.1");
		tester.testDecoder("0.1", "0.1");
		tester.testDecoder("0.01", "0.01");
		tester.testDecoder("10.1", "10.1");

		// Decimal (-)
		tester.testDecoder("-1.1", "-1.1");
		tester.testDecoder("-0.1", "-0.1");

		// Truncated decimal (repeating)
		tester.testDecoder("0.428571428571428571...", "0.428571428571428571...");

		// Un-parsable
		tester.testDecoder("abc", null);
		tester.testDecoder("1.x", null);
	}
