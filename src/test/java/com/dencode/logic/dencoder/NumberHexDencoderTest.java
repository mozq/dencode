	@Test
	public void test_decoder() {
		// Blank
		tester.testDecoder("", null);

		// Integer
		tester.testDecoder("0", "0");
		tester.testDecoder("1", "1");
		tester.testDecoder("a", "10");
		tester.testDecoder("64", "100");
		tester.testDecoder("63", "99");

		// Integer (-)
		tester.testDecoder("-1", "-1");
		tester.testDecoder("-a", "-10");
		tester.testDecoder("-64", "-100");

		// Integer (+)
		tester.testDecoder("+1", "1");
		tester.testDecoder("+a", "10");

		// Decimal
		tester.testDecoder("1.1", "1.0625"); // 1 + 1/16
		tester.testDecoder("0.1", "0.0625");
		tester.testDecoder("0.01", "0.00390625"); // 1/256
		tester.testDecoder("a.1", "10.0625");

		// Decimal (-)
		tester.testDecoder("-1.1", "-1.0625");
		tester.testDecoder("-0.1", "-0.0625");

		// Truncated decimal (repeating)
		tester.testDecoder("0.6db6db6db...", "0.428571428571428571...");

		// Un-parsable
		tester.testDecoder("g", null);
		tester.testDecoder("abcg", null);
		tester.testDecoder("1.g", null);
	}
